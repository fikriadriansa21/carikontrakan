    private void tablePengontrakMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePengontrakMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = tablePengontrak.getSelectedRow();
        model = (DefaultTableModel) tablePengontrak.getModel();
        tfIdPengontrak.setText((String) model.getValueAt(selectedRowIndex, 0));
        tfIdPengontrak.setEditable(false);
        tfNik.setText((String) model.getValueAt(selectedRowIndex, 1));
        tfNamaPengontrak.setText((String) model.getValueAt(selectedRowIndex, 2));
        tfNoHp.setText((String) model.getValueAt(selectedRowIndex, 3));
    }//GEN-LAST:event_tablePengontrakMouseClicked

